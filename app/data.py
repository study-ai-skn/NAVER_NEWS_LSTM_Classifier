def _scrape_naver_news(sid: str, label: str, max_items: int = 100) -> List[Tuple[str, str]]:
    """네이버 뉴스 섹션 페이지를 페이지네이션으로 순회해 뉴스 제목을 수집한다."""
    try:
        import requests
        from bs4 import BeautifulSoup

        headers = {
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 Chrome/120.0.0.0 Safari/537.36",
            "Accept-Language": "ko-KR,ko;q=0.9",
        }
        results: List[Tuple[str, str]] = []
        page = 1
        while len(results) < max_items:
            url = f"https://news.naver.com/main/list.naver?mode=LSD&mid=sec&sid1={sid}&page={page}"
            resp = requests.get(url, headers=headers, timeout=10)
            resp.raise_for_status()
            soup = BeautifulSoup(resp.text, "html.parser")
            page_items = [
                tag.get_text(strip=True)
                for tag in soup.select("dt a")
                if len(tag.get_text(strip=True)) > 5
            ]
            if not page_items:   # 더 이상 기사 없으면 종료
                break
            for text in page_items:
                results.append((text, label))
                if len(results) >= max_items:
                    break
            page += 1
        return results
    except Exception:
        return []


